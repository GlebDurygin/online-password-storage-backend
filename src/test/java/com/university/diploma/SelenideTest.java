    protected void executeTest(WebDriver driver, String str) {
        driver.get("http://localhost:3000/sign-up");
        driver.findElement(By.xpath("//*[@id=\"root\"]/div/div/div[2]/div/div[1]/div/div[3]/div[2]/form/div[1]/input"))
                .sendKeys(str);
        driver.findElement(By.xpath("//*[@id=\"root\"]/div/div/div[2]/div/div[1]/div/div[3]/div[2]/form/div[2]/input"))
                .sendKeys(str);
        driver.findElement(By.xpath("//*[@id=\"root\"]/div/div/div[2]/div/div[1]/div/div[3]/div[3]/button")).click();

        driver.get("http://localhost:3000/sign-in");

        driver.findElement(By.xpath("//*[@id=\"root\"]/div/div/div[2]/div/div[1]/div/div[3]/div[2]/form/div[1]/input"))
                .sendKeys(str);
        driver.findElement(By.xpath("//*[@id=\"root\"]/div/div/div[2]/div/div[1]/div/div[3]/div[2]/form/div[2]/input"))
                .sendKeys(str);
        driver.findElement(By.xpath("//*[@id=\"root\"]/div/div/div[2]/div/div[1]/div/div[3]/div[3]/button"))
                .click();
    }
